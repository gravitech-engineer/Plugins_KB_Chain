void OLED_128X64_I2C::clear(void) {
	// clear draw
	memset(vram, 0x00, (OLED_I2C_WIDTH * (OLED_I2C_HEIGHT / 8)));
	// set render flag
	set_flag |= (0x80 | OLED_I2C_RENDER_FLAG);
}

esp_err_t OLED_128X64_I2C::render(I2CDev *i2c) {
	int page, i, j;
	unsigned char buf[17], *p;
	esp_err_t ret;

	switch (chipset) {
		case ocs_ssd1306:
			buf[0] = 0; // command
			buf[1] = OLED_REG_LOW_COLUMN | 0x0; // low col = 0 (ssd1306)
			if ((ret = i2c->write(channel, address, buf, 2)) != ESP_OK) {
				return ret;
			}
			buf[1] = OLED_REG_HIGH_COLUMN | 0x0; // hi col = 0
			if ((ret = i2c->write(channel, address, buf, 2)) != ESP_OK) {
				return ret;
			}
			buf[1] = OLED_REG_SETSTARTLINE | 0x0; // line #0
			if ((ret = i2c->write(channel, address, buf, 2)) != ESP_OK) {
				return ret;
			}

			p = vram;

			for (i = 0; i < 64; i ++) {
				buf[0] = 0x40; // data
				for (j = 1; j < 17; j++) {
					buf[j] = *p++;
				}
				if ((ret = i2c->write(channel, address, buf, 17)) != ESP_OK) {
					return ret;
				}
			}
			break;

		case ocs_sh1106:
			// sh1106 => use 8 pages of 16*8 bytes for 1 lines
			for (page = 0; page < 8; page++) {
				buf[0] = 0; // command
				buf[1] = OLED_REG_LOW_COLUMN | 0x2; // low col = 2 (sh1106)
				if ((ret = i2c->write(channel, address, buf, 2)) != ESP_OK) {
					return ret;
				}
				buf[1] = OLED_REG_HIGH_COLUMN | 0x0; // hi col = 0
				if ((ret = i2c->write(channel, address, buf, 2)) != ESP_OK) {
					return ret;
				}
				buf[1] = OLED_REG_SETSTARTLINE | 0x0; // line #0
				if ((ret = i2c->write(channel, address, buf, 2)) != ESP_OK) {
					return ret;
				}
				buf[1] = OLED_REG_SET_PAGEADDR | page; // page n
				if ((ret = i2c->write(channel, address, buf, 2)) != ESP_OK) {
					return ret;
				}

				p = vram;
				p += 16 * 8 * page;

				// render 1 line per page, 16*8 bytes
				for (i = 0; i < 8; i++) {
					buf[0] = 0x40; // data
					for (j = 1; j < 17; j++) {
						buf[j] = *p++;
					}
					if ((ret = i2c->write(channel, address, buf, 17)) != ESP_OK) {
						return ret;
					}
				}
			}
			break;
	}

	return ESP_OK;
}

void OLED_128X64_I2C::process(Driver *drv) {
	I2CDev *i2c = (I2CDev *)drv;
	uint8_t buf[3], len;

	switch (state) {
		case s_detect:
			// detect i2c device
			if (i2c->detect(channel, address) == ESP_OK) {
				init_ptr = &oled_i2c_init_array[0];
				state = s_cmd_init;
			}
			else {
				state = s_error;
			}
			break;

		case s_cmd_init:
			if (init_ptr->command != -1) {
				buf[0] = 0; // command
				buf[1] = init_ptr->command;
				len = 2;
				if (init_ptr->data != -1) {
					buf[2] = init_ptr->data;
					len = 3;
				}
				// i2c write
				if (i2c->write(channel, address, buf, len) == ESP_OK) {
					init_ptr++;
				}
				else {
					state = s_error;
				}
			}
			else {
				// clear screen
				clear();
				// render
				if (render(i2c) == ESP_OK) {
					// display on
					buf[0] = 0; // command
					buf[1] = OLED_REG_DISPLAY_ON;
					if (i2c->write(channel, address, buf, 2) == ESP_OK) {
						// clear error flag
						error = false;
						// set initialized flag
						initialized = true;
						// goto idle state
						state = s_idle;
					}
					else {
						state = s_error;
					}
				}
				else {
					state = s_error;
				}
			}
			break;

		case s_idle:
			// check set/clr flag
			if (clr_flag & 0x7f) {
				FLAG_CLR(flag, clr_flag & 0x7f);
				clr_flag = 0;
			}
			if (set_flag & 0x7f) {
				FLAG_SET(flag, set_flag & 0x7f);
				set_flag = 0;
			}

			// check pending flag
			if (IS_FLAG_SET(flag, OLED_I2C_RENDER_FLAG)) {
				FLAG_CLR(flag, OLED_I2C_RENDER_FLAG);
				// render
				if (render(i2c) != ESP_OK) {
					state = s_error;
				}
			}
			break;

		case s_error:
			// set error flag
			error = true;
			// clear initialized flag
			initialized = false;
			// get current tickcnt
			tickcnt = get_tickcnt();
			// goto wait and retry with detect state
			state = s_wait;
			break;

		case s_wait:
			// delay 1000ms before retry detect
			if (is_tickcnt_elapsed(tickcnt, 1000)) {
				state = s_detect;
			}
			break;
	}
}

void OLED_128X64_I2C::putch(uint8_t lcd_column, uint8_t lcd_row, char val) {
    int i, indx, buf_indx;

	// font 6x8
    if ((lcd_row < (OLED_I2C_HEIGHT / 8)) && (lcd_column < (OLED_I2C_WIDTH / 6))) {
		buf_indx = (lcd_row * OLED_I2C_WIDTH) + (lcd_column * 6);
	    indx = val * 6;
	    for (i = 0; i < 6; i++) {
			vram[buf_indx + i] = oled_font_6_8[indx];
	        indx++;
	    }
	}

	// set render flag
	set_flag |= (0x80 | OLED_I2C_RENDER_FLAG);
}

void OLED_128X64_I2C::putstr(uint8_t lcd_column, uint8_t lcd_row, char *str) {
	int i, indx, buf_indx;

	// font 6x8
    if ((lcd_row < (OLED_I2C_HEIGHT / 8)) && (lcd_column < (OLED_I2C_WIDTH / 6))) {
		buf_indx = (lcd_row * OLED_I2C_WIDTH) + (lcd_column * 6);
		while (*str) {
			indx = *str * 6;
			for (i = 0; i < 6; i++) {
				vram[buf_indx + i] = oled_font_6_8[indx];
				indx++;
			}
			str++;
			buf_indx += 6;
		}
	}

	// set render flag
	set_flag |= (0x80 | OLED_I2C_RENDER_FLAG);
}

void OLED_128X64_I2C::putstr(uint8_t lcd_column, uint8_t lcd_row, double val) {
	char buf[(OLED_I2C_WIDTH / 6) + 1];
	int i;

	snprintf(buf, (OLED_I2C_WIDTH / 6), "%.2f", val); // default precision = 2
	// check ending zero
	i = strlen(buf) - 1;
	while (i >= 0) {
		if (buf[i] == '0') {
			buf[i] = '\x0';
		}
		else
		if (buf[i] == '.') {
			buf[i] = '\x0';
			break;
		}
		else {
			break; // first occurence of non-zero or dot
		}
		i--;
	}

	putstr(lcd_column, lcd_row, buf);
}

